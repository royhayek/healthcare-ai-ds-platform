# ── Diagnosis triage (multiclass + fairness) ───────────────────────────────────


class TestDiagnosisTriage:
    fixture = FIXTURES_DIR / "diagnosis_triage.csv"
    target = "diagnosis_category"

    @pytest.fixture(scope="class")
    def pipeline(self):
        return _run_pipeline(self.fixture, target=self.target)

    def test_fixture_exists(self):
        assert self.fixture.exists(), f"Fixture missing: {self.fixture}"

    def test_class_count(self):
        df = pd.read_csv(self.fixture)
        assert df[self.target].nunique() == 4, "Expected 4 triage categories"

    def test_class_balance_reasonable(self):
        df = pd.read_csv(self.fixture)
        counts = df[self.target].value_counts(normalize=True)
        # No class should dominate >60% or be below 10%
        assert counts.max() < 0.60
        assert counts.min() > 0.10

    def test_task_is_multiclass(self, pipeline):
        assert pipeline["task_type"] == "multiclass"

    def test_predictions_multiclass(self, pipeline):
        unique_preds = set(pipeline["y_pred"].tolist()) if hasattr(pipeline["y_pred"], "tolist") else set(pipeline["y_pred"])
        assert len(unique_preds) >= 2, "Multiclass model should predict at least 2 classes"

    def test_stability_runs_complete(self, pipeline):
        assert len(pipeline["stability"].scores) == 4  # 2 seeds × 2 folds

    def test_demographic_columns_present(self):
        df = pd.read_csv(self.fixture)
        # gender and insurance_type are required for intersectional fairness tests
        assert "gender" in df.columns
        assert "insurance_type" in df.columns

    def test_fairness_audit_runs(self, pipeline):
        from backend.ml.fairness import build_sensitive_features, fairness_audit

        df = pd.read_csv(self.fixture)
        y_test = np.asarray(pipeline["y_test"])
        y_pred = np.asarray(pipeline["y_pred"])
        n = len(y_test)

        if "gender" in df.columns and n > 0:
            gender_df = df[["gender"]].iloc[:n].reset_index(drop=True)
            sensitive = build_sensitive_features(gender_df, ["gender"])
            report = fairness_audit(
                y_true=y_test,
                y_pred=y_pred,
                y_proba=None,
                sensitive_features=sensitive,
            )
            assert report is not None
            assert hasattr(report, "overall_severity")
            assert report.overall_severity in ("none", "mild", "moderate", "severe")


# ── Cross-fixture: PHI redaction in audit payloads ────────────────────────────


class TestPhiAuditRedaction:
    def test_redact_removes_ssn(self):
        from backend.core.audit import redact_phi_from_payload
        payload = {"note": "Patient SSN: 123-45-6789, age 45"}
        result = redact_phi_from_payload(payload)
        assert "123-45-6789" not in result["note"]
        assert "[REDACTED]" in result["note"]
        assert "age 45" in result["note"]  # non-PHI text preserved

    def test_redact_preserves_metric_values(self):
        from backend.core.audit import redact_phi_from_payload
        payload = {"auc": 0.872, "threshold": 0.31, "model": "XGBClassifier", "n_rows": 5000}
        result = redact_phi_from_payload(payload)
        assert result == payload  # nothing redacted

    def test_clinical_range_flags_survive_profile(self):
        from backend.ml.profiler import check_clinical_ranges
        import pandas as pd
        df = pd.DataFrame({"hemoglobin": [8.0, 14.5, 200.0]})
        flags = check_clinical_ranges(df)
        assert "hemoglobin" in flags
        assert flags["hemoglobin"]["clinical_concern"] is True
